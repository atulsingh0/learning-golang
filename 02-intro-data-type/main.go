package main

import "fmt"

func main() {
	fmt.Println("Strings:  wow")
	fmt.Println("Strings with new line \\n:  Good\nMorning")
	fmt.Println("Strings with quote:  \"wow\"")
	fmt.Println("Strings with tab \\t:  wow\tme")
	fmt.Println("Strings with backslash \\:  New line char \\n")

	fmt.Println("Numbers:", 34)
	fmt.Println("Numbers:", 3.14)
	fmt.Println("Runes:", 'A')
	fmt.Println("Bool:", true)

}
